public class Test2 {
  public static void main(String[] args) {
    int arr[] = {10,20,30,40};
    // forEach loop
    // for each element inside the array
    for (int a:arr)
    {
      System.out.println(a);
    }


    for (int i = 0; i <arr.length ; i++) {
      System.out.println(arr[i]);
    }
  }
}
